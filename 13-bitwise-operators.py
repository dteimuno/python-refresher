#disjunction
# |
#returns one if either bit is one
print(15 | 22)
#exclusive or operator ^
#returns one if either bit is one but not both
print(15 ^ 22)
#negation
print(~15)
#returns one for every zero, and zero for every one
#bit shifting
print(22 >> 1)  # right shift = divides by 2
print(22 << 1)  # left shift = multiplies by 2