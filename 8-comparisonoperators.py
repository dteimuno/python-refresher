#in python six comparison operators
# are available: ==, !=, >, <, >=, <=
print(2 == 2)
print(2 == 4)
print("Hello" == "Hello")
print("Hello" == "Goodbye")
print(4 == (2 * 2))

#not equal
print(2 != 2)
print(2 != 4)
print("Hello" != "Hello")
print("Hello" != "Goodbye")
print(4 != (2 * 2))

#greater than
print(4 > 2)
print(2 > 4)
print(2 > 2)
cost_of_apple = 2
cost_of_banana = 3
print(cost_of_apple > cost_of_banana)
print(cost_of_apple < cost_of_banana)